// 4 pilares da POO

// Encapsulamento -> agrupar coisas similares dentro de um objeto, evita acessar os atributos diretamente, preferencia acessar indiretamente

// Abstração -> esconder a complexidade do usuario, executa funcoes complexas dentro da classe, e da ao usuário apenas metodos simples

// Herança - subclasse e extenções de classes, objetos complexos de forma simples

// Polimorfismo -> pega metodos herdados, e modifica para que o usuário não perceba difereças de implementação, e possa utilizar os mesmos métodos para fazer o que a subclasse precisa